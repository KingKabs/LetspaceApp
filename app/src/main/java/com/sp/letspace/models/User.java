package com.sp.letspace.models;

public class User {
    public int id;
    public String name;
    public String email;
    public String role;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }
}
